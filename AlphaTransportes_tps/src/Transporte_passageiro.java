public class Transporte_passageiro {
    private int qtd_de_passageiros;
    private int qtd_bagagem;
    private double peso_bagagem;

    public int getQtd_de_passageiros() {
        return this.qtd_de_passageiros;
    }

    public void setQtd_de_passageiros(int qtd_de_passageiros) {
        this.qtd_de_passageiros = qtd_de_passageiros;
    }

    public int getQtd_bagagem() {
        return this.qtd_bagagem;
    }

    public void setQtd_bagagem(int qtd_bagagem) {
        this.qtd_bagagem = qtd_bagagem;
    }

    public double getPeso_bagagem() {
        return this.peso_bagagem;
    }

    public void setPeso_bagagem(double peso_bagagem) {
        this.peso_bagagem = peso_bagagem;
    }

}
