public class Carro {
    private int registro;
    private String modelo;
    private int num_chassi;
    private String placa;
    private int capacidade_de_passageiros;

    public int getRegistro() {
        return this.registro;
    }

    public void setRegistro(int registro) {
        this.registro = registro;
    }

    public String getModelo() {
        return this.modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getNum_chassi() {
        return this.num_chassi;
    }

    public void setNum_chassi(int num_chassi) {
        this.num_chassi = num_chassi;
    }

    public String getPlaca() {
        return this.placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public int getCapacidade_de_passageiros() {
        return this.capacidade_de_passageiros;
    }

    public void setCapacidade_de_passageiros(int capacidade_de_passageiros) {
        this.capacidade_de_passageiros = capacidade_de_passageiros;
    }
}
